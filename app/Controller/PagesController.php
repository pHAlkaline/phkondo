App::uses('AppController', 'Controller');

class PagesController extends AppController {

    /**
     * Controller name
     *
     * @var string
     */
    public $name = 'Pages';

    /**
     * This controller does not use a model
     *
     * @var array
     */
    public $uses = array();

    /**
     * Displays a view
     *
     * @param mixed What page to display
     * @return void
     */
    public function display() {
        $path = func_get_args();

        if (!empty($path[0]) && $path[0] == 'home' && !(bool)  AuthComponent::user()) {
            $path[0] = 'login';
            $this->layout = "login";
        }
        $count = count($path);
        if (!$count) {
            $this->redirect('/');
        }
        $page = $subpage = $title_for_layout = null;

        if (!empty($path[0])) {
            $page = $path[0];
        }
        if (!empty($path[1])) {
            $subpage = $path[1];
        }
        if (!empty($path[$count - 1])) {
            $title_for_layout = $headerTitle = __(Inflector::humanize($path[$count - 1]));
        }
        $this->set(compact('page', 'subpage', 'title_for_layout', 'headerTitle'));
        $this->render(implode('/', $path));
    }
    
    public function beforeRender() {
        parent::beforeRender();
        $breadcrumbs = array(
            array('link' => Router::url(array('controller' => 'pages', 'action' => 'index')), 'text' => __('Home'), 'active' => ''),
                
        );
       
        if ($this->viewVars['page']=='login'){
        $breadcrumbs[1]=array('link'=>'','text'=>__('Start Session'),'active'=>'active');
            
        }
        $headerTitle=__('Start Session');
        $this->set(compact('breadcrumbs','headerTitle'));
    }
    
    
   

}
